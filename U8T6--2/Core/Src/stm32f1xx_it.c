// CAN接收缓冲区
volatile uint8_t can_rx_buffer[8];
volatile uint8_t can_rx_flag = 0;
volatile uint32_t can_rx_id = 0;

// CAN调试变量 - 在Debug窗口观察
volatile uint32_t can_irq_count = 0;       // CAN中断进入次数
volatile uint32_t can_rx_success_count = 0; // CAN接收成功次数
volatile uint32_t can_rx_fail_count = 0;    // CAN接收失败次数
volatile uint32_t can_last_error = 0;       // 最后一次错误码

// CAN接收完成回调
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    can_irq_count++;  // 中断计数
    
    CAN_RxHeaderTypeDef rx_header;
    uint8_t rx_data[8];
    
    if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rx_header, rx_data) == HAL_OK)
    {
        // 只处理标准ID的数据帧（允许任意DLC）
        if (rx_header.IDE == CAN_ID_STD && rx_header.RTR == CAN_RTR_DATA)
        {
            uint8_t dlc = rx_header.DLC;
            if (dlc > 8) dlc = 8;
            memset((void*)can_rx_buffer, 0, 8);
            memcpy((void*)can_rx_buffer, rx_data, dlc);
            can_rx_id = rx_header.StdId;
            can_rx_flag = 1;
            can_rx_success_count++;
        }
        else
        {
            can_rx_fail_count++;
            can_last_error = 1;  // ID类型不匹配或远程帧
        }
    }
    else
    {
        can_rx_fail_count++;
        can_last_error = 2;  // HAL_CAN_GetRxMessage失败
    }
}

